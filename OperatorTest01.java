package operator;

public class OperatorTest01 {

	public static void main(String[] args) {
		// 대입연산자 단순대입(=)  복합대입(+= -=...)
		int result = 0;
		result += 10;
		System.out.println(result);
		result -= 5;
		System.out.println(result);
		result *= 5;
		System.out.println(result);
		result /= 5;
		System.out.println(result);
		result %= 3;
		System.out.println(result);
		
		
		
		
		
		

	}

}
